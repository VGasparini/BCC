    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        Bibliotecario secao = Facade.get().login(usuarioField.getText(), senhaField.getText());
        if (secao != null){
            Facade.get().logged = secao;
            FormMenu formMenu = new FormMenu();
            formMenu.setVisible(true);
            this.hide();
        } else{
            // pop-up
            JOptionPane.showMessageDialog(this, "Login Incorreto", "Erro", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_loginButtonActionPerformed

