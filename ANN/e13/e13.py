N = [
    [-1.72780521453553],
    [-1.857323531180396],
    [-1.838165618719436],
    [-1.828890843079051],
    [-1.826266689843919],
    [-1.825591142423036],
]

grau = 6
b = 2

for i in range(grau - 1):
    for j in range(grau - i - 1):
        N[j].append(
            ((2 ** (i * b + b) * N[j + 1][i]) - N[j][i]) / (2 ** (i * b + b) - 1)
        )
print("N_%d(1) = %.15f" % (grau, N[0][-1]))
